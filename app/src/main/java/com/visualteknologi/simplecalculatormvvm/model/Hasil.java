package com.visualteknologi.simplecalculatormvvm.model;

public class Hasil {
    String hasilTambah;
    String hasilKurang;
    String hasilKali;
    String hasilBagi;

    public String getHasilTambah() {
        return hasilTambah;
    }

    public void setHasilTambah(String hasilTambah) {
        this.hasilTambah = hasilTambah;
    }

    public String getHasilKurang() {
        return hasilKurang;
    }

    public void setHasilKurang(String hasilKurang) {
        this.hasilKurang = hasilKurang;
    }

    public String getHasilKali() {
        return hasilKali;
    }

    public void setHasilKali(String hasilKali) {
        this.hasilKali = hasilKali;
    }

    public String getHasilBagi() {
        return hasilBagi;
    }

    public void setHasilBagi(String hasilBagi) {
        this.hasilBagi = hasilBagi;
    }
}
